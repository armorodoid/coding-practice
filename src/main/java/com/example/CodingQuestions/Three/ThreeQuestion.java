package com.example.CodingQuestions.Three;

public class ThreeQuestion {
    /*
    Check whether the number is even or odd
    Add both even and odd separately and return their difference

    nums = [1,2,3,4,5,6,7,8,9]
    evenSum = 2 + 4 + 6 + 8  = 20
    oddSum = 1 + 3 + 5 + 7 + 9 = 25

    Output = difference of evenSum and oddSum = | 20-25 | = 5
     */
    public int diffBetweenEvenAndOdd(int[] nums) {
        ThreeSolution threeSolution = new ThreeSolution();
        return threeSolution.diffBetweenEvenAndOdd(nums);
    }
}
